package package1;

public class VolumeMeasure {
	   public double v;
	   //public double V;
	   public double pi=3.1416;
	   
	   public double measureVolume(double r, double h) {
	        
		 return v = pi*(r*r)*h;  //Volume of Cylinder v=πr2h
		 
		 
	    }	
	   public double measureVolume(double r) {
	        
			 return v = (4/3)*pi*(r*r*r);  //Volume of sphere V=4/3πr3
	   }
       public double measureVolume(int a) {
			        
		     return v = a*a*a;  //VVolume of Cube V=a3
       } 
};
	   
